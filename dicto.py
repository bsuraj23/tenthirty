# Define a dictionary with fruits and their colors
fruit_colors = {
    "apple": "red",
    "banana": "yellow",
    "grapes": "purple",
    "orange": "orange",
    "kiwi": "green"
}

# Display the fruits and their colors
print("Fruit Colors:\n")
for fruit, color in fruit_colors.items():
    print(f"The color of {fruit} is {color}.")
