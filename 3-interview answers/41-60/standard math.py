import math

# Example usage of math functions
number = 16
angle = math.pi / 4  # 45 degrees in radians

# 1. Square root
sqrt_result = math.sqrt(number)

# 2. Sine function
sin_result = math.sin(angle)

# 3. Factorial
factorial_result = math.factorial(5)

# Display results
print(f"Square root of {number} is {sqrt_result}")
print(f"Sine of 45 degrees (Ï€/4 radians) is {sin_result:.4f}")
print(f"Factorial of 5 is {factorial_result}")
