#len() - Get length of a list
names=["chinnu", "minnu", "bannu"]
print("number of names:", len(names))

#type() - Get type of a variable
x=10.5
print("Type of a x:", type(x))

#max() and min() - Get maximum and minimum from a list
numbers=[4, 9, 2, 7, 5]
print("Maximum number:", max(numbers))
print("Minimum number:", min(numbers))

#sum() - Sumof list elements
print("Sum of numbers:", sum(numbers))

#sorted() - Returns a sorted list
print("Sorted numbers:", sorted(numbers))

#str(), int(), float() - Type conversion
a=5
b=float(a)
c=str(a)
print("Integer:", a, "Float:", b, "String:", c)

#abs() - Absolute value
negative_number=-15
print("Absolute value:", abs(negative_number))

#round() - Round a number
pi=3.14159
print("Rounded value of pi:", round(pi, 2))

#pow() - power of a number
print("2 raised to power 3 is:", pow(2, 3))