#Integer
num=10
print("Value:", num)
print("Type:", type(num))

#Float
price=99.99
print("Value:", price)
print("Type:", type(price))

#String
name="Sindhu"
print("Name:", name)
print("Type:", type(name))

#Boolean
is_active=True
print("Active:", is_active)
print("Type:", type(is_active))

#List
colors=["red", "green", "blue"]
print("Colors:", colors)
print("Type:", type(colors))

#Tuple
person=("Archu", 22)
print("Person:", person)
print("Type:", type(person))

#Set
unique_numbers={1,2,3}
print("Set:", unique_numbers)
print("Type:", type(unique_numbers))

#Dictionary
student={"name": "Swathi", "age": 22}
print("Student:", student)
print("Type:", type(student))

#None
x=None
print("Value:", x)
print("Type:", type(x))