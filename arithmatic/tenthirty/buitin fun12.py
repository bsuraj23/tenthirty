#Using len() – Find Length of a List
fruits = ["apple", "banana", "cherry"]
print("Total fruits:", len(fruits))

# Using sum() – Add All Elements in a List
numbers = [10, 20, 30, 40]
total = sum(numbers)
print("Sum of numbers:", total)

# Using max() and min()
scores = [85, 92, 78, 96]
print("Max score:", max(scores))
print("Min score:", min(scores))

# Using sorted() – Sort a List
names = ["Zara", "Aman", "John"]
sorted_names = sorted(names)
print("Sorted names:", sorted_names)


# Using any() and all() with numbers

numbers = [0, 5, 10]
print("All non-zero?", all(numbers))  
print("Any non-zero?", any(numbers))  
