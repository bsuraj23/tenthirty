# Working with Functions - 6 Examples

# Example 1: Function to print a message
def greet():
    print("Hello from function!")

greet()

# Example 2: Function to add two numbers
def add(a, b):
    return a + b

print(add(5, 3))

# Example 3: Function to multiply numbers
def multiply(a, b):
    return a * b

print(multiply(4, 5))

# Example 4: Function to check even number
def is_even(num):
    return num % 2 == 0

print(is_even(10))

# Example 5: Function to print a message passed
def print_message(msg):
    print(msg)

print_message("Learning Functions is Fun!")

# Example 6: Function to find square
def square(num):
    return num * num

print(square(6))
