# Arguments - 6 Examples

# Example 1
def welcome(name):
    print("Welcome", name)

welcome("John")

# Example 2
def total_cost(price, quantity):
    return price * quantity

print(total_cost(20, 5))

# Example 3
def power(base, exponent):
    return base ** exponent

print(power(2, 3))

# Example 4
def divide_numbers(a, b):
    return a / b

print(divide_numbers(10, 2))

# Example 5
def subtraction(x, y):
    return x - y

print(subtraction(50, 20))

# Example 6
def percentage(marks, total):
    return (marks / total) * 100

print(percentage(450, 500))
