# Parameters - 6 Examples

# Example 1
def welcome(name):
    print("Welcome", name)

# Example 2
def total_cost(price, quantity):
    return price * quantity

# Example 3
def power(base, exponent):
    return base ** exponent

# Example 4
def divide_numbers(a, b):
    return a / b

# Example 5
def subtraction(x, y):
    return x - y

# Example 6
def percentage(marks, total):
    return (marks / total) * 100
