# Function Creation - 6 Examples

# Example 1
def hello():
    print("Hello!")

# Example 2
def add_numbers(a, b):
    return a + b

# Example 3
def print_name(name):
    print("Name:", name)

# Example 4
def divide(x, y):
    return x / y

# Example 5
def area_circle(radius):
    return 3.14 * radius * radius

# Example 6
def repeat_message(msg, times):
    for _ in range(times):
        print(msg)
